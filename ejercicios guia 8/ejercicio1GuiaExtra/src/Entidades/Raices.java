package Entidades;

/**
 *
 * @author jonat
 */
public class Raices {
    int m;
    String nombre;

    public Raices(int m, String nombre) {
        this.m = m;
        this.nombre = nombre;
    }

    public Raices() {
    }

    public int getM() {
        return m;
    }

    public void setM(int m) {
        this.m = m;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}
